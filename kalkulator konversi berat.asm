NAME "CALCULATE KONVERSI SATUAN BERAT"
INCLUDE 'emu8086.inc'
#MAKE_COM#
ORG 100H
JMP MULAI:
PSN1    DB "CALCULATE KONVERSI SATUAN BERAT",13,10
        DB "DARI SATUAN TON, MENJADI QUINTAL, DAN KILOGRAM",13,10
        DB "ANGKA YANG DIMASUKKAN HARUS BILANGAN BULAT",13,10,'$'
PSN2    DB 13,10,13,10,'MASUKKAN NILAI SATUAN TON: $'
PSN3    DB "NILAI DALAM SATUAN QUINTAL: $"
PSN4    DB "NILAI DALAM SATUAN KILOGRAM: $"
PSN5    DB 13,10,13,10,"TERIMA KASIH TELAH MENGGUNAKAN PROGRAM CALCULATE..."
        DB 13,10,13,10,"TEKAN SEMBARANG UNTUK KELUAR $"
TON     DW ?
QUINTAL DW ?
KG      DW ?

MULAI:
LEA DX,PSN1
MOV AH,9
INT 21H

LEA DX,PSN2
MOV AH,9
INT 21H

CALL SCAN_NUM

MOV TON,CX

PUTC 13
PUTC 10

LEA DX,PSN3
MOV AH,9
INT 21H

MOV AX,TON
MOV BX,10
IMUL BX


MOV QUINTAL,AX


CALL PRINT_NUM


PUTC 13
PUTC 10

LEA DX,PSN4
MOV AH,9
INT 21H

MOV AX,TON
MOV BX,1000 
IMUL BX

MOV KG ,AX


CALL PRINT_NUM


PUTC 13
PUTC 10

LEA DX,PSN5
MOV AH,9
INT 21H

MOV AH,0
INT 16H

RET

DEFINE_SCAN_NUM
DEFINE_PRINT_NUM
DEFINE_PRINT_NUM_UNS
END